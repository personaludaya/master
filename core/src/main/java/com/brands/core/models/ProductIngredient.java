package com.brands.core.models;

public class ProductIngredient {

    private String ingredientPagePath;
    private String ingredientTxt;
    private String ingredientVolume;

    public String getIngredientPagePath() {
        return ingredientPagePath;
    }

    public void setIngredientPagePath(String ingredientPagePath) {
        this.ingredientPagePath = ingredientPagePath;
    }

    public String getIngredientTxt() {
        return ingredientTxt;
    }

    public void setIngredientTxt(String ingredientTxt) {
        this.ingredientTxt = ingredientTxt;
    }

    public String getIngredientVolume() {
        return ingredientVolume;
    }

    public void setIngredientVolume(String ingredientVolume) {
        this.ingredientVolume = ingredientVolume;
    }
}
